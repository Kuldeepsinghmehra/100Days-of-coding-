public class Patterns {
    public static void main(String[] args) {
        //pattern 1 
        // * * *
        // * * *
        // * * *
        for(int i=0;i<5;i++)
        {
            for(int j=0;j<5;j++)
            {
                System.out.print("*");
            }
            System.out.println();
        }
    }
    
}
